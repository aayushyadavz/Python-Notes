# Standard Arithematic Operators
print(10 + 3)  # 13
print(10 - 3)  # 7
print(10 * 3)  # 30
print(10 / 3)  # 3.3333333333333335
print(10 // 3)  # 3
print(10 % 3)  # 1
print(10 ** 3)  # 1000

# For all these operators, we have a special operator called the augmented assignment operator
x = 10
x += 3  # i.e. x = x + 3
